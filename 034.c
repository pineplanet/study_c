#include <stdio.h>

int main(void)
{
	int a = 50;
	int b; 
	b =100;
	a =200;

	puts("--------------------------------");
	puts("* 값의 대입(assignment)");
	puts("--------------------------------");

	printf(" a= %d,b=%d \n", a,b);
	puts("--------------------------------");

	puts(" = 는 대입, == 는 같다는 의미");

	return 0;
}
