#include <stdio.h>

int main(void)
{
	int a = 33, b= 77;
	int tmp;

	puts("-------------------------------");
	puts(" * 정수 맞 교환 *");
	puts("-------------------------------");

	printf(" 원래 값 a= %d, b=%d \n",a,b);

	puts("-------------------------------");

	tmp = a;
	a = b;
	b = tmp;

	printf(" \n 교환 후 a = %d, b=%d \n", a,b);
	puts("-------------------------------");

	puts(" 값을 맞교환시 반드시 빈그릇 필요");
	puts("-------------------------------");

	return 0;
}
